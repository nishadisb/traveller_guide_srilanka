<h1>Laxapana Falls</h1>
<p>
  The name's derived from the words "Laksha-pahana", with "Laksha" denoting 100,000 and "Pahana" meaning lamps. Ergo, 100,000 lamps; which was apparently a reference to how the water diverted from here could be used to generate electricity to light up that many lamps. <br>

Moving away from history lessons and trivia, let's get back to the trip. We were waterfall-hopping one fine weekend, and Laxapana was one of the four or so we wanted to check out. Mostly because of its size, mostly because it's supposed to be hella photogenic, and mostly because well, it's a waterfall in the middle of the cooler climes in Sri Lanka.
<br>
Laxapana waterfall is situated on the Maskeliya-Norton Bridge Road in a village named Kiriwan Eliya. It gives its name to two hydroelectric power stations namely Old Laxapana Power Station and New Laxapana Power Station. The Laxapana Waterfall is formed by the Maskeliya Oya which is a tributary to the Kelani River. It is quite a difficult waterfall to hike and caution should be taken when taking a dip in the natural pools in the area due to the flow of the water.
