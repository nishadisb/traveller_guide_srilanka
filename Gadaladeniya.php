<h1>Gadaladeniya Vihara</h1>
<h2>History</h2>


<p>
  
The history of the temple goes back to the 14th century. According to historical reports this temple was built in 1344, under the patronage of King Bhuvanekabahu IV, who reigned from 1341 to 1351 A. D.,[3] by the Ven. Seelavamsa Dharmakirti.[4] The architecture of the temple was designed following Hindu artistic styles by, Ganesvarachari, a South Indian architect.[5] On the rock outcrop, upon which the temple stands, is a carved inscription regarding the temple's construction.[4] The temple was constructed in the Dravidian architectural style[6] incorporating Sinhalese architecture from the Polonnaruwa era and other Indo Chinese architectural patterns.[7]

Celebrated scholarly monks who have resided at the temple include Sangharaja Dharmakirti II, the author of the Sangharaja-Nikāya (14-15th century)[8] and Vimalakirti I.[4]

The temple was abandoned until King Vira Parakrama Narendra Sinha (1707-1739) handed over it to Weliwita Sri Saranankara Thero,[9] whose pupils have looked after the temple ever since.
<br>
<h2>Main shrine</h2>
The central temple building is unique in that it is built entirely of sculptured granite, except for two shikharas which are built of brick, and sits on a massive stone slab.[2] The sikharas, which are located above the sanctum begins in an octagonal shape but has been given the features of a dagoba. After the original construction of the temple building, possibly in the 18th or 19th century, a multi-tiered and tiled Sinhalese roof, resting on brick piers and wooden pillars, was added to the temple, concealing the flat roof and the shikharas.[4]

At the entrance to the shrine room is an elaborately ornate sculptured Makara Thorana (Dragon's Arch).[11] The Makara Thorana is decorated with images of Brahmā, Śakra, Santuṣita, Saman, Santhusthika, Natha and Maitreya.[12] Inside the shrine room is a 2.43 m (8.0 ft) high statue of a seated Buddha, in the 'Dhyana Mudra’ pose, the posture of meditation associated with his first enlightenment,[11] with four standing Buddha statues flanking it. The statue is significantly different from those of the Gampola period (1314–1415).

Adjacent to the main shrine room, on its north side, is a devale dedicated to the Hindu god Vishnu. The devale was originally dedicated to Varuna when it was built however as Vishnu became more popular with devotees the shrine to Varuna was replaced with Vishnu.

<h2>Secondary shrine</h2>
In the middle of the temple compound is a secondary shrine, the Vijayothpaya or Vijayantha Prasada named after the mythical palace of god Indra. The shrine was constructed after the main shrine by King Parakramabahu V.[13] The shrine consists of a central stupa, approximately 12.3 m (40 ft) high built on a square platform.[2] The entire stupa is covered by a four-sided roof supported by four stone pillars. On the four sides of the main stupa are four smaller matching stupas, extending in cruciform angles out in the four cardinal directions. Each of these four smaller stupas are built on stone platforms with their own individual shrine rooms.[5][14] These shrines formerly housed images of the Four Heavenly Kings: Vaishravaṇa, Virūḷhaka, Dhrutharashṭa and Virūpaksha[12] but now houses statues of the seated Buddha..</p>
