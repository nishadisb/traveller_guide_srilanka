<h1>Muthumariamman Temple</h1>
<p>The prefix “Muthu”, literally means “pearl”. "Mari" means rain and "Amman" means mother in Tamil language.[2] The temple is dedicated to Mariamman, the goddess of rain and fertility.
   The temple was severely damaged during the anti-Tamil riots in July 1983 but has been subsequently restored. One of the most visually dominant features of this temple is its 32.9 m (108 ft) high Gopuram ('Raja Koburum'), a large decorated tower located above the main northern gateway ('Vadakku Vaayil') to the temple. 

<h2>History</h2>


The land was originally part of a paddy field and was gifted by the owner in 1852. The current temple was built in 1874, funded by the Nattukkottai Chettiar.[4] This temple is used by both Hindus and Buddhists. The temple was originally a small statue under a tree prayed to by the Hindu people but has been developed by the people in Matale.[5]

The temple was severely damaged during the anti-Tamil riots in July 1983 but has been subsequently restored. One of the most visually dominant features of this temple is its 32.9 m (108 ft) high Gopuram ('Raja Koburum'), a large decorated tower located above the main northern gateway ('Vadakku Vaayil') to the temple. The Raja Koburum is one of the largest Gopurams in Sri Lanka. The 1008 statues of Hindu deities are the work of South Indian sculptor, Nagalingam and his son Ramanathan, with the help of about 100 designers, painters and architects from Tamil Nadu and Sri Lanka. The Raja Koburum was completed in 2007 at the cost of approximately Rs 150M..</p>
