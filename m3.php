<h1>Sembuwaththa lake</h1>
<p>In between the 100 - 89 Century BC, during the reign of King Walagamba, Sri Lanka underwent a famine known as 'Baminithiyasaya' for 12 years. There was also a South Indian invasion at the same time and the Buddhist monks of that era realized that these problems would be a danger to the existence of Buddha Sasana in the country. Under these conditions, memorizing and repeating the Dhamma (doctrine) was difficult. Due to the famine Buddhist monks did not receive sufficient alms and faced many difficulties, and they had to eat yams, roots and leaves of trees for their survival. A group of about 60 of them went to Malaya Rata, which is said to be the hilly area of the country. They managed to live on the banks of Mahaweli river under harsh conditions and survived for 12 years till the famine came to an end. King Walagamba who was overthrown by a rebellion during the invasion from South India, regained the throne by defeating the invaders after fourteen years.

The monks who left to India and to the hilly areas of Sri Lanka, during the difficult period, returned to Anuradhapura, and decided to transcribe the Tripiṭaka (philosophical doctrines of Buddhism) for the preservation and for the use of future generations. The monks selected Aluvihare Rock temple in Matale as the most suitable and secured place to carry out this important event. This transcription was carried out due to the fear that the doctrine would be lost during the upheaval caused by repeated South Indian invasions.[5] It is said that 500 scholarly monks congregated at Aluvihare Rock temple to perform the difficult task of first reciting the doctrines and agreeing on an acceptable version before transcription. The entire transcription was done in books made of ola leaves, locally known as puskola poth. These books were made up from thick strips created from the leaves of either the palmyra or talipot palm and the doctrines were written down in Pali language. A metal stylus was used to inscribe the characters on the ola leaves.

The old library atof Aluvihare Rock Temple, which had safely housed the volumes of this transcribed manuscripts for so many centuries, was totally destroyed during the Matale Rebellion in 1848.[6] Many parts of the temple complex was destroyed too by this incident. The consequences of this disaster are still evident today at the temple premises. It took a long a time for the recompilation, as few generations of monks had to transcribe the Tripiṭaka again. The recompilation and transcription took a long time as only few monks were engaged in this painstaking task, and the first of the three “baskets of the law” was only completed in 1982.

Aluvihare Rock temple has many caves with ancient inscriptions. These caves enclose comparatively modern wall and ceiling paintings of interest, and impressive statues and images of Lord Buddha. The main cave of these, has a large reclining statue of the Buddha, together with standing and seated images. In the entrance-way of another cave with a reclining statue of the Buddha there are terrifying depictions of the hellish afterlife that awaits people who commits sins. One cave is dedicated to the revered Indian monk-scholar Buddhagosa,[7] who is regarded as the greatest exponent and interpreter of the Pali canonical scriptures. He resided at Anuradhapura during the 4th and 5th centuries and is supposed to have spent several years at the Matale Alu Viharaya to engage in his scholarly work.</p>
