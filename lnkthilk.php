<h1>Lankatilaka Vihara</h1>
<h2>History</h2>
<p>The history of the temple goes back to the 14th century. According to historical reports this temple was built by King Bhuvanekabahu IV, who reigned from 1341 to 1351 A. D.[5] He entrusted the construction of this temple to his Chief Minister named Senalankadhikara, who successfully finished the works of this temple. The architecture of the temple was designed by a South Indian architect named Sathapati Rayar.[6] According to the Professor Senarath Paranavithana, Sathapati Rayar designed this temple using Sinhalese architecture of Polonnaruwa era and also with other Dravidian and Indo Chinese architectural patterns
</p><br>
<p>
 <h2>The temple</h2>

A Buddha image with Makara Thorana
The vihara buildings have been built on a natural rock called Panhalgala Rock. Among the buildings the image house possess characteristically outstanding architectural features, embellished with traditional Sinhalese sculptures.[8] According to the facts recorded in the Lankatilake copper plaque, this image house was construct as a four storied mansion with height of eighty feet, but today only 3 stories can be seen.[7] The walls and the ceiling of the image house has been adorned with the Kandyan era paintings and sculptures.[9]

Rock curved inscriptions found in the temple premises with both Sinhala and Tamil sections, proclaim about the initiators and the facilities gifted to this temple by the kings.

The Lankatilaka Raja Maha Vihara (also known as the Lankatilaka Temple or the Blue Temple), is located in Hiyarapitiya in the Udunuwara area of Kandy, Sri Lanka’s hill capital. It was constructed as a four storey building that stood eighty feet high and sits on an uneven rock bed overlooking the Hantane mountain range. The history of Lankatilaka Vihara dates back to the Gampola era and was constructed during that time under the patronage of King Buwanekabahu IV in 1344 AD.<br>
Lankatilaka is a most unique structure in itself, having been built entirely atop an uneven rock surface and rising upto a height of eighty feet. Based on inscriptions to be found on the Temple’s copper plaque, one learns that the foundation of granite was laid on the rock surface and the rest of the structure was made of bricks. Beautiful arches and sculptures enhance the thick outer walls, making this an architectural marvel of that era. There are many inscriptions in both Sinhala and Tamil languages cut into the solid rock which describe the facilities provided to the Temple and the land gifted by Kings who ruled then.
