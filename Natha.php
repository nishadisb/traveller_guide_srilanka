<h1> Natha Devale</h1>
<p>
The Nàtha Devàla is located on the terrace, in front of the Palace complex of Kandy. It is said to have been in existence even before the Tooth Relic was brought to Kandy.The oldest extant structure in Kandy, it is said to have been built by King Vikramabahu III in the 14th century.[1] The history of the worship of God Natha is not clear. The name 'Natha' literally means ‘no form’ and ‘no shape’, and is therefore generally associated with Lord Maitreya, the next Buddha.<br><br><br>

  In the days of the Kingdom of Kandy, this shrine is said have played an important role in the establishment of the royalty. The king was given his royal name at this shrine. The Bodhisattva Avalokitesvara, being worshiped as a curer of diseases, the tradition of distributing herbal preparations on New Year day was conducted here until recent times. Hence, Bodhisattva Avalokitesvara is also associated with the Natha Devale, Kandy. In the annual procession, the Natha Devale procession takes the pride of place among the Devale processions and follows just behind the Dalada Perahara.<br><br>

Vishnu Devale of Kandy, a shrine which is located to the north of the Natha Devale, is in the inner complex of the palace. That shrine is dedicated to God Vishnu, one of the Hindu Triad, is considered a sober divinity who was committed to the protection of Buddhism from the beginning. He is the god that protects Sri Lanka, thus if he protects Sri Lanka he protects Buddhism.<br>

Lord Buddha prophesied that Buddhism would flourish for 5,000 years.More than 2500 years of that 5000 years has gone past. It is said that Lord Natha is still in the battle of protecting Buddhism from Mara, and he has sent gods from the heaven to earth to protect Buddhism.
<br>
The “Natha Devale” is situated directly facing the Dalada Maligaya and this building is the oldest surviving structure from the Kandyan Kingdom. The devale was built by King Vickramabahu III in the 14th Century. The building has a Dravidian architecture with a “gedige” style. The structure has 3 stories but the 1st and the 2nd floor levels serves no practical purpose. The roof is a dome with a shape of a stupa. The Devale has two stupas and a recent excavations have revealed a circular shape basement probably a Mandapa which has been destroyed by invaders. The stupa near the Devale is thought to contain the bowl relic of Buddha and was broken in to by Relic Hunters in 1889.
