<h1>Ambuluwawa Tower</h1>
<h2>History</h2>
<p>Avoid leaning on the railing to capture photographs especially at the top as this is extremely risky and one could easily slip off from here. Ambuluwawa is a relatively easy hike to do which gives you 360 degree views of the surrounding mountains, forests, rivers and towns.
</p><br>
<p>
It is an area richly diverse, with evergreen forests, blossomed flower plants as well creepers and about 200 different kinds of plants from 80 plant families including a variety of medicinal plants. Ambuluwawa is surrounded by many mountains including Piduruthalagala from the East, Bible Rock (Bathalegala) from the West, Sri Pada (Adam’s Peak) from the South and Knuckles Mountain Range from the North. Apart from these mountains many other mountains are also clearly visible from here and adds to the beauty of the area. These are the Hanthana Mountain Range, Hunnasigiri Mountain and mountain ranges associated with Algalla and Kadugannawa. There is a cool mountain breeze in the area due to this reason.<br>
</p>
<p>
  
  The Ambuluwawa Hill and the adjacent forest is designated as a forest reservation which is named as the Ambuluwawa ICC Forest Reservation. The history of the Ambuluwawa Temple dates back to the 13th century during the reign of King Buwanekabahu IV. It is said that the Ambuluwawa Peak was the center of the Gampola Kingdom during his reign. But there is nothing with historical or archaeological values in here.Ambuluwawa is situated approximately 1,965 feet from the level of Gampola town. The Ambuluwawa Mountain stands approximately 3560 meters above sea level. The tower stands at a height of approximately 48 meters and the elderly and kids are not recommended to climb to the top of it. The mountain road runs along the serpentine surrounded by mountain landscapes, cliffs and unique plants, which cannot be found anywhere else. Interesting places to visit include three ponds, a sacred Bodhi tree, water Park, stone Park and garden of herbs.
<br>
The Ambuluwawa Biodiversity Center was inaugurated by Sri Lanka’s former Prime Minister, Dissanayake Mudiyanselage Jayaratne, popularly known as “Di Mu”, who was born near Gampola. For those going from Colombo, Ambuluwawa is situated 85 km from the capital city.
</p>
