<h1>Bahirawakanda Viharaya</h1>
<p>The Bahirawakanda Vihara Buddha Statue displays Lord Buddha seated in the Nirvana pose and can be viewed from the entire city of Kandy. The best time to climb up the hill for spectacular views of the Kandy city is at night when the entire city is lit up with lights.
<br>

The Sri Maha Bodhi Viharaya at Bahirawa Kanda is where the giant Buddha Statue is built and this was founded by Ven Ampitiye Dammarama Thero, who started the monastery in 1972. The Buddha Statue which is in a seated Nirvana position is 88 feet tall and is one of the largest Buddha Statues in Sri Lanka and is sculptured in pure white that emits a sense of serenity and tranquility all round. Many devotees throng the Bahirawa Kanda Temple during all times of the year.  This beautiful Buddha Statue can be viewed from all points from the Kandy town, which makes it a unique phenomenon.

Visitors can get a beautiful view of the Kandy town, including other places of interest from the Bahirawa Kanda Temple precincts. The temple is open for worship by devotees. All those who enter the temple are supposed to remove their shoes and hats and be dressed in attire that is befitting the sacred area. <br>
Some tourists believe that there isn’t anything important. Rather than that, a trip to the bahiravakanda vihara Buddha statue will b a spectacular visit to Kandy’s famous sites. Be sure to remove your caps and shoes or slippers before entering the premise. Devotees are expected to worship Buddha statue by offering flowers and lighting oil lamps. Henceforward the site is being developed to attract more tourists.
