<h1>Seetha Amman kovil</h1>
<p>
  Located just 1 km from the Hakgala is the Seetha Amman kovil. This is the only Seetha Amman kovil in the world and is built according to South Indian architecture. Legend has it that Seetha was held captive and hidden in this spot by king Rama to protect her from the demon king Ravana according to the Epic Ramayana. The stream that runs close to this spot is believed to be the place where Sita bathed and the rock beside it is supposed to be the place where she sat praying. <br><br>.On the rock face across the stream are circular depressions believed to be the foot prints of Rawana’s elephant.There is one spot in the stream where the water cannot be drunk and legend has it that Sita cursed this spot and hence the bad taste. Water is very clear and the Temple is known in many other names such as Sita Amman kovil, Sita Amman Temple, Hanuman Kovil, Hanuman Temple and Sri Bhaktha Hanuma Kovil which all refer to the same place.

  <h3>Significance of Seetha Amman temple </h3> 
The soil here is black in colour. According to the epic of Ramayana it is the believed that Hanumanji burnt this area before he left Sri Lanka. The foot prints of Lord Hanuman’s can be seen even today close to the stream close to the Sita Amman Temple. <br>.

<h3>Legend related to Sita Amman temple</h3>.
With reference to the Ramayana proof of Sri Lanka it is believed that the Sita devi was kept captive at this site by King Ravana. There is a stream by the side of the temple where that runs from the hill, catered to the needs of Sita Devi during her stay at Ashok Vatika. She is also said to have bathed in this stream. About a century ago of three idols were discovered in the stream.
