<h1>Wasgamuwa National Park</h1>
<p>The National Park's annual daily temperature is 28 °C (82 °F) and has a dry zone climate.[1] Annual rainfall ranges between 1650–2100 mm. Rain is received during the north-eastern monsoon, from October to January.[4] July–September is the dry season. Highest elevation of the National Park is Sudu Kanda (White mountain), which is 470 metres (1,540 ft) of height. The soil of the national park contains quartz and marble. The forests of Wasgamuwa represent Sri Lanka dry-zone dry evergreen forests.[5] The park consists of primary, secondary, riverine forests and grasslands.</p>

<h2>History and historic irrigation</h2>

<p>Ruins of Malagamuwa, Wilmitiya, Dasthota irrigation tanks and Kalinga Yoda Ela canal which are built by Parākramabāhu I remain in the national park.[1] In the past water was irrigated from the Minipe anicut's left bank canal to Parakrama Samudra by Amban ganga which had run through Wasgamuwa.[4]

Yudangana Pitiya has identified as the battleground of the battle between King Ellalan and King Dutthagamani taken place.[4] A grassland that the Dutthagamani's army supposed to have camped before the battle is known as Kandauru Pitiya. The ruins of Chulangani chaitya which is built by King Mahanaga can be seen in the national park. Its circumference, 966 feet (294 m) is greater than the Ruwanwelisaya's.[1] The artifacts that have been recovered from the bricks of the chaitya include a bowl used by King Sri Vikrama Rajasinha and several bronze statues are now kept in the Yudangana vihara.</p>
<h2>Flora</h2>
<p>Wasgamuwa National Park exhibits one of the highest biodiversity among the protected areas in Sri Lanka.[1] More than 150 floral species have recorded from the park. Cryptocoryne walkeri and Munronia pumila are two plants with economic value. Reservoirs and riverine forests support large number of fauna species. The forest consists of several layers. Chloroxylon swietenia, Manilkara hexandra, Elaeodendron glaucum, Pterospermum canescens, Diospyros ebenum, Holoptelea intergrifolia, Pleurostylia opposita, Vitex altissima, Drypetes sepiaria, and Berrya cordifolia are dominant trees in the emergent layer. Polyalthia korinti, Diplodiscus verrucosus, Limonia acidissima, Cassia roxburghii and Strobilanthes stenoden are common in other strata. Some 1,700 years old tamarind tree, "Oru Bendi Siyambalawa" (Sinhala for Canoes-Moored-Tamarind) was situated in the park.[6]</p>
<h2>Fauna</h2>
<p>Wasgamuwa National Park is home to 23 species of mammals.[1] The park is inhabited by a herd of 150 Sri Lankan elephants. Marsh elephant (Elephas maximus vil-aliya) roams in the Mahaweli river area. Both monkeys found in the park, purple-faced langur and toque macaque, are endemic to Sri Lanka. While water buffalo and Sri Lankan axis deer are common to observe, Sri Lanka leopard and sloth bear are rare. Small golden palm civet is another rare endemic mammal.[4]</p>


