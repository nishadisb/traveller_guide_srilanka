<h1>Nalanda Gedige</h1>
<p>Also some scholars describe this building is a dravidian architecture dedicated to a Mahayanaa cult with pronounced Trantric learning and known for an ancient monument of possible Vajrayana (Tantric) Buddhist affinities.[3]

Nalanda Gedige is designed on the lines of a Hindu temple with a mandapa, an entrance hall (originally roofed), a short passage to a bare cello, and an ambulatory round the holy center. A limited number of the original Hindu deity statuettes exist within the temple, however, a statue of the God Kubera appears on the south side of the tympanum over the sanctuary, a feature that can only be seen in Sri Lanka.

The richly decorated facade sections, laboriously reassembled in 1975, are predominantly in the South Indian style. Although they cannot be precisely dated, they are believed to have originated sometime between the 8 to 11th centuries.</p>


<h2>History</h2>
This building made was 1000 years ago. This was a period of great turmoil on the island with South Indian kings establishing themselves in the wake of the decline of the Sinhalese monarchy. It is possible that Nalanda Gedige was a bold attempt at a fusion of Sinhalese cultures.

History of Nalanda Gedige as an archaeological site began in 1893, when, according to then Archaeological Commissioner, H. C. P. Bell, "land was acquired round this little-known and solitary shrine of granite construction, popularly styled gedige. It is situated on raised ground in paddy fields, picturesquely surrounded by low hills and wooded hamlets. In 1911 a small gang was detached from the labor force at Sigiriya to thoroughly root out all the jungle growth upon and around the ruin besides cutting still further back the earth silt hiding the bold stylites upon which the fane stands. Very special importance attaches to this unique temple, as it is the sole example yet discovered in Ceylon of composite styles of architecture judiciously blended to form a delightfully homogeneous edifice.
