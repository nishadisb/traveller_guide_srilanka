<h1>Dambulla cave temple</h1>
<h2>History</h2>
<p>Dambulla is the largest and best-preserved cave temple complex in Sri Lanka. The rock towers 160 m over the surrounding plains. There are more than 80 documented caves in the surrounding area. Major attractions are spread over five caves, which contain statues and paintings. These paintings and statues are related to Gautama Buddha and his life. There are a total of 153 Buddha statues, three statues of Sri Lankan kings and four statues of gods and goddesses. The latter include Vishnu and the Ganesha. The murals cover an area of 2,100 square metres (23,000 sq ft). Depictions on the walls of the caves include the temptation by the demon Mara, and Buddha's first sermon.


</p><br>
<p>
Prehistoric Sri Lankans would have lived in these cave complexes before the arrival of Buddhism in Sri Lanka as there are burial sites with human skeletons about 2700 years old in this area, at Ibbankatuwa near the Dambulla cave complexes.<br>
</p>
<h2>History</h2>
<p>
  
 This temple complex dates back to the first century BCE.[2] It has five caves under a vast overhanging rock, carved with a drip line to keep the interiors dry. In 1938 the architecture was embellished with arched colonnades and gabled entrances. Inside the caves, the ceilings are painted with intricate patterns of religious images following the contours of the rock. There are images of the Lord Buddha and bodhisattvas, as well as various gods and goddesses.

The Dambulla cave monastery is still functional and remains the best-preserved ancient edifice in Sri Lanka. This complex dates from the third and second centuries BC, when it was already established as one of the largest and most important monasteries. Valagamba of Anuradhapura is traditionally thought to have converted the caves into a temple in the first century BC. Exiled from Anuradhapura, he sought refuge here from South Indian usurpers for 15 years. After reclaiming his capital, the King built a temple in thankful worship. Many other kings added to it later and by the 11th century, the caves had become a major religious centre and still are. Nissanka Malla of Polonnaruwa gilded the caves and added about 70 Buddha statues in 1190. During the 18th century, the caves were restored and painted by the Kingdom of Kandy.
<br>
<h2>The five caves</h2>
The temple is composed of five caves of varying size and magnificence.[3] The caves, built at the base of a 150m high rock during the Anuradhapura (1st century BC to 993 AD) and Polonnaruwa times (1073 to 1250), are by far the most impressive of the many cave temples found in Sri Lanka. Access is along the gentle slope of the Dambulla Rock, offering a panoramic view of the surrounding flat lands, which includes the rock fortress Sigiriya, 19 km away. Dusk brings hundreds of swooping swallows to the cave entrance. The largest cave measures about 52m from east to west, and 23m from the entrance to the back, this spectacular cave is 7m tall at its highest point. Hindu deities are also represented here, as are the kings Valagamba and Nissankamalla, and Ananda - the Buddha's most devoted disciple.</p>
